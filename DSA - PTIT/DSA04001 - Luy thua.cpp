const ll mod = 1e9 + 7;
ll pow(ll a, ll b) {
	ll res = 1;
	while (b > 0) {
		if (b % 2 == 1) {
			res *= a;
			res %= mod;
		}
		a *= a;
		a %= mod;
		b /= 2;
	}
	return res;
}
