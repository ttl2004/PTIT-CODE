	cout.tie(0);
	int t;
	cin >> t;
	while (t --) {
		int n;
		cin >> n;
		int a[n + 5], sum = 0, max_value = -1e9;
		for (int i = 0; i < n; i ++) {
			cin >> a[i];
			sum += a[i];
			max_value = max(max_value, a[i]);
		}
		if (max_value > sum / 2 || sum % 2 == 1) cout << "NO\n";
		else cout << "YES\n";
	}

	return 0;
