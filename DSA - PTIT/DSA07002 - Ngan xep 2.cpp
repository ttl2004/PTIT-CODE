int main(){
	ios_base :: sync_with_stdio (0);
	cin.tie(0);
	cout.tie(0);
	int t;
	cin >> t;
	stack<int> st;
	while (t --) {
		string s;
		cin >> s;
		if (s == "PUSH") {
			int x;
			cin >> x;
			st.push(x);
		}
		else if (s == "POP") {
			if (!st.empty()) st.pop();
		}
		else {
			if (st.empty()) cout << "NONE";
			else  cout << st.top();
			cout << endl;
		}
	}
	return 0;
}
