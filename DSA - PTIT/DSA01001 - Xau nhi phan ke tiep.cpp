	while (t --) {
		string s;
		cin >> s;
		int len = s.size();
		int i = len - 1;
		while (i > 0 && s[i] == '1') i --;
		if (i <= 0) {
			for (int j = 0; j < len; j ++) cout << "0";
			cout << endl;
		}
		else {
			s[i] = '1';
			for (int j = i + 1; j < len; j ++) s[j] = '0';
			cout << s << endl;
		}
	} 

