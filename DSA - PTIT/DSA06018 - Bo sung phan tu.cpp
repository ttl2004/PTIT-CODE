int main(){
	ios_base :: sync_with_stdio (0);
	cin.tie(0);
	cout.tie(0);
	int t;
	cin >> t;
	while (t --) {
		int n;
		cin >> n;
		map<int, int> mp;
		int min_value = 1e9, max_value = 0;
		for (int i = 0; i < n; i ++) {
			int x;
			cin >> x;
			mp[x] ++;
			min_value = min(min_value, x);
			max_value = max(max_value, x);
		}
		int count = 0;
		for (int i = min_value; i <= max_value; i ++) {
			if (mp[i] == 0) count ++;
		}
		cout << count << endl;
	}
	return 0;
}
