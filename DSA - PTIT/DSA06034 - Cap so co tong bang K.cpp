int main(){
	ios_base :: sync_with_stdio (0);
	cin.tie(0);
	cout.tie(0);
	int t;
	cin >> t;
	while (t --) {
		int n, k;
		cin >> n >> k;
		ll cnt = 0;
		unordered_map<ll, int> ump;
		for (int i = 0; i < n; i ++) {
			ll x;
			cin >> x;
			if (ump.find(k - x) != ump.end()){
				cnt += ump[k - x];
			}
			ump[x] ++;
		}
		cout << cnt << "\n";
	}
	return 0;
}
