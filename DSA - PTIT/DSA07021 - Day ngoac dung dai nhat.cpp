int main(){
	ios_base :: sync_with_stdio (0);
	cin.tie(0);
	cout.tie(0);
	int t;
	cin >> t;
	while (t --) {
		string s;
	    cin >> s;
	    stack<int> st;
	    st.push(-1);
		int count = 0;
		for (int i = 0; i < s.size(); i++) {
			if (s[i] == '(') st.push(i);
			else {
				st.pop();
				if (st.size()>0) {
					count = max(count, i - st.top());
				}
				if (st.empty()) st.push(i);
			}
		}
		cout << count << endl;
	}
	return 0;
}
