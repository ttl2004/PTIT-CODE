#include <bits/stdc++.h>
#define ll long long
#define pb push_back
#define endl '\n'
using namespace std;

int main() {
	ios_base :: sync_with_stdio (0);
    cin.tie(0);
    cout.tie(0);
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;
        int cnt = 0;
        for (int i = 1; i*i <= n; i++) {
            if (n % i == 0){
                int j = n/i;
                if (i % 2 == 0 && j % 2 == 0) cnt+=2;
                else if (i % 2 == 0) cnt++;
                else if (j % 2 == 0) cnt++;
            }
        }
        cout << cnt << endl;
    }
	return 0;
}