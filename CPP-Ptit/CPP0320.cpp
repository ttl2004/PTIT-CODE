int main() {
	ios_base :: sync_with_stdio (0);
    cin.tie(0);
    cout.tie(0);
    int t;
    cin >> t;
    while (t--) {
        string s;
        cin >> s;
        map<char, bool> cnt;
        int kt = 1;
        if (s[0] == '0') kt = 0;
        for (char x : s) {
            if(x >= '0' && x <= '9') {
                cnt[x] = true;
            }
            else {
                kt = 0;
                break;
            }
        }
        if (kt == 0) cout << "INVALID\n";
        else {
            if (cnt.size() == 10) cout << "YES\n";
            else cout << "NO\n";
        }
    }
	return 0;
}