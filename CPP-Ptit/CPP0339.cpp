#include <bits/stdc++.h>
#define ll long long
#define pb push_back
#define endl '\n'
using namespace std;

int main() {
	ios_base :: sync_with_stdio (0);
    cin.tie(0);
    cout.tie(0);
    int t;
    cin >> t;
    while (t--) {
        string s;
        cin >> s;
        int cnt = s.size();
        for (int i = 0; i < s.size(); i++) {
            char c = s[i];
            int idx = i + 1;
            while (s.find(c, idx) != -1) {
                idx = s.find(c, idx) + 1;
                cnt++;
            }
        }
        cout << cnt << endl;
    }
	return 0;
}