#include <bits/stdc++.h>
#define ll long long
#define pb push_back
#define endl '\n'
using namespace std;

int main() {
	ios_base :: sync_with_stdio (0);
    cin.tie(0);
    cout.tie(0);
    int t;
    cin >> t;
    while (t--) {
        ll n, m;
        cin >> n >> m;
        ll  a[n + 5], b[m + 5], _max = -1e9, _min = 1e9;
        for (int i = 0; i < n; i++) {
            cin >> a[i];
            _max = max(_max, a[i]);
        }
        for (int i = 0; i < m; i++) {
            cin >> b[i];
            _min = min(_min, b[i]);
        }
        cout << _max * _min << endl;
    }
	return 0;
}