#include <bits/stdc++.h>
#define ll long long
#define pb push_back
#define endl '\n'
using namespace std;

int main() {
	ios_base :: sync_with_stdio (0);
    cin.tie(0);
    cout.tie(0);
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;
        int _min = 1e9, _max = -1e9;
        map<int, int> cnt;
        for (int i = 0; i< n; i++) {
            int x;
            cin >> x;
            cnt[x]++;
            _min = min(_min, x);
            _max = max(_max, x);
        }
        int dem = 0;
        for (int i = _min; i <= _max; i++) {
            if (!cnt[i]) dem++; 
        }
        cout << dem << endl;
    }
	return 0;
}