#include <bits/stdc++.h>
#define ll long long
#define pb push_back
#define endl '\n'
using namespace std;

int main() {
	ios_base :: sync_with_stdio (0);
    cin.tie(0);
    cout.tie(0);
    int t;
    cin >> t;
    while (t--) {
        string s;
        cin >> s;
        int k; 
        cin >> k;
        int cnt[26] = {};
        for (char x : s) {
            cnt[x-'a']++;
        }
        int dem = 0;
        for (int i = 0; i < 26; i++) {
            if (cnt[i] == 0) dem++;
        }
        cout << (s.size() >= 26 && k >= dem) << endl;
    }
	return 0;
}